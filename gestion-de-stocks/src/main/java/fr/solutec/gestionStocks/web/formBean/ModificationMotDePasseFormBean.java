package fr.solutec.gestionStocks.web.formBean;

/**
 * @author achankimponne
 * 
 */
public class ModificationMotDePasseFormBean {

	/**
	 * Mot de passe actuel
	 */
	private String mpActuel;

	/**
	 * Nouveau mot de passe
	 */
	private String mpNouveau;

	/**
	 * Confirmation du nouveau mot de passe
	 */
	private String mpConfirmationNouveau;

	public ModificationMotDePasseFormBean() {

	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		StringBuffer string = new StringBuffer(
				ModificationMotDePasseFormBean.class.getName());

		string.append("[");
		string.append("mpActuel=" + mpActuel).append(",mpNouveau=" + mpNouveau)
				.append(",mpConfirmationNouveau=" + mpConfirmationNouveau);
		string.append("]");

		return string.toString();
	}

	/**
	 * @return the mpActuel
	 */
	public String getMpActuel() {
		return mpActuel;
	}

	/**
	 * @param mpActuel
	 *            the mpActuel to set
	 */
	public void setMpActuel(String mpActuel) {
		this.mpActuel = mpActuel;
	}

	/**
	 * @return the mpNouveau
	 */
	public String getMpNouveau() {
		return mpNouveau;
	}

	/**
	 * @param mpNouveau
	 *            the mpNouveau to set
	 */
	public void setMpNouveau(String mpNouveau) {
		this.mpNouveau = mpNouveau;
	}

	/**
	 * @return the mpConfirmationNouveau
	 */
	public String getMpConfirmationNouveau() {
		return mpConfirmationNouveau;
	}

	/**
	 * @param mpConfirmationNouveau
	 *            the mpConfirmationNouveau to set
	 */
	public void setMpConfirmationNouveau(String mpConfirmationNouveau) {
		this.mpConfirmationNouveau = mpConfirmationNouveau;
	}
}
